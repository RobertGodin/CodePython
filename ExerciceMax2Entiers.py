"""
Exemple afficher le maximum de deux entiers
"""

entier1 = int(input("entrez un nombre entier: "))
entier2 = int(input("entrez un nombre entier: "))
if (entier1 > entier2):
    print("Le maximum des deux entiers est :", entier1)
else :
    print("Le maximum des deux entiers est :", entier2)
