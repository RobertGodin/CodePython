''' Exercice1.py
Ce programme saisit trois entiers et en affiche la somme '''

# Saisir les deux chaînes de caractères qui représentent des nombres entiers
chaine1 = input("Entrez un premier nombre entier :")
chaine2 = input("Entrez un second nombre entier :")
chaine3 = input("Entrez un troisième nombre entier :")


# Convertir les trois chaînes de caractères en entiers
entier1 = int(chaine1)
entier2 = int(chaine2)
entier3 = int(chaine3)

# Calculer la somme des trois entiers
somme = entier1 + entier2 + entier3

# Afficher la somme 
print("La somme des trois entiers est ",somme)
